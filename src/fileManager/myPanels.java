	public panel panel;
	public pnlSetting pnlSetting;
	public crclSetting crclSetting;
	public collisionTime collisionTime;
	public primary primary;
	public saveInfo saveInfo;
	public loadInfo loadInfo;
	
	public panel getPanel() {
		return panel;
	}
