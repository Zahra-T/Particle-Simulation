package simulation;

public class lineEquation {
	public double a;
	public double b;
	public double c;
	public Point start;
	public Point end;
	public lineEquation(double a, double b, double c, Point start, Point end)
	{
		this.a = a;
		this.b = b;
		this.c = c;
		this.start = start;
		this.end = end;
	}
}
